    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int base10 = 0;
        int baseDestino, baseOrigem;
        String resultado = "";
        
        baseOrigem = Integer.parseInt(origem.getSelectedItem().toString());
        
        //Converter incialmente para a base 10
        if(baseOrigem == 10) {
            base10 = Integer.parseInt(numero.getText());
        }
        else{
            String num = numero.getText();
            int valor;
            base10 = 0;
            for(int i=0; i<num.length(); i++) {
                switch(num.charAt(i)) {
                    case 'A': valor = 10; break;
                    case 'B': valor = 11; break;
                    case 'C': valor = 12; break;
                    case 'D': valor = 13; break;
                    case 'E': valor = 14; break;
                    case 'F': valor = 15; break;
                    default: valor = Integer.parseInt("" + num.charAt(i)); break;
                } 
                base10 += valor*Math.pow(baseOrigem, num.length()-i-1);
            }
        }
        
        baseDestino = Integer.parseInt(destino.getSelectedItem().toString());
        
        //Convertendo para a base de destino
        if(baseDestino == 10) {
            saida.setText(String.valueOf(base10));
        }
        else {
            while (base10/baseDestino != 0){
                switch(base10%baseDestino) {
                    case 10: resultado += "A"; break;
                    case 11: resultado += "B"; break;
                    case 12: resultado += "C"; break;
                    case 13: resultado += "D"; break;
                    case 14: resultado += "E"; break;
                    case 15: resultado += "F"; break;
                    default: resultado += base10%baseDestino; break;
                }
                base10 /= baseDestino; 
            }
            
            switch(base10%baseDestino) {
                    case 10: resultado += "A"; break;
                    case 11: resultado += "B"; break;
                    case 12: resultado += "C"; break;
                    case 13: resultado += "D"; break;
                    case 14: resultado += "E"; break;
                    case 15: resultado += "F"; break;
                    default: resultado += base10%baseDestino; break;
                }
            
            StringBuilder sb = new StringBuilder(resultado);
            sb.reverse();
            resultado = sb.toString();
            saida.setText(resultado);
        }
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

    private void validarNumero(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_validarNumero
        String texto = numero.getText().toUpperCase();
        
        int pos = texto.length()-1;
        char c = texto.charAt(pos);
        int n;
        
        int baseOrigem= Integer.parseInt(origem.getSelectedItem().toString());
        
        try {   
            n = Integer.parseInt("" + c);
        }
        catch(NumberFormatException e) {
            n = baseOrigem;
        }
        
        if (baseOrigem == 16) {
            if (c != 'A' && c != 'B' && c != 'C' && c != 'D' && c != 'E' && c != 'F' && n == baseOrigem)
                texto = texto.substring(0, pos);
        } else {
            if(n >= baseOrigem)
                texto = texto.substring(0, pos);
        }

        numero.setText(texto);
    }//GEN-LAST:event_validarNumero

